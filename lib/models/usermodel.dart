class UserCredentials {
  String id;
  String firstname;
  String password;
  String lastname;
  String email;
  String position;
  String phoneNumber;
  String country;
  String address;
  bool notification;
  String image;
  String token;

  UserCredentials({
    required this.id,
    required this.firstname,
    required this.password,
    required this.lastname,
    required this.email,
    required this.position,
    required this.phoneNumber,
    required this.country,
    required this.address,
    required this.notification,
    required this.image,
    required this.token,
  });
}
